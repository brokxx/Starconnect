// ===== LOGIQUE I18N =====
(function() {
    var SUPPORTED = ['fr', 'en', 'ar', 'bn'];
    var DEFAULT_LANG = 'en';
    var STORAGE_KEY = 'starconnect_lang';

    function detectLang() {
        var stored = localStorage.getItem(STORAGE_KEY);
        if (stored && SUPPORTED.indexOf(stored) !== -1) return stored;

        var browser = (navigator.language || navigator.userLanguage || '').toLowerCase();
        for (var j = 0; j < SUPPORTED.length; j++) {
            if (browser.indexOf(SUPPORTED[j]) === 0) return SUPPORTED[j];
        }
        return DEFAULT_LANG;
    }

    function applyLang(lang) {
        if (SUPPORTED.indexOf(lang) === -1) lang = DEFAULT_LANG;
        var dict = TRANSLATIONS[lang];
        if (!dict) return;

        // text content
        var nodes = document.querySelectorAll('[data-i18n]');
        nodes.forEach(function(node) {
            var key = node.getAttribute('data-i18n');
            if (dict[key]) node.textContent = dict[key];
        });

        // attributes (placeholder, title, alt, aria-label)
        var attrNodes = document.querySelectorAll('[data-i18n-attr]');
        attrNodes.forEach(function(node) {
            var pairs = node.getAttribute('data-i18n-attr').split(',');
            pairs.forEach(function(pair) {
                var parts = pair.split(':');
                if (parts.length === 2 && dict[parts[1]]) {
                    node.setAttribute(parts[0], dict[parts[1]]);
                }
            });
        });

        // html lang + dir
        document.documentElement.setAttribute('lang', lang);
        document.documentElement.setAttribute('dir', lang === 'ar' ? 'rtl' : 'ltr');

        // page title
        if (dict['home.title'] && document.body.classList.contains('home')) {
            document.title = dict['home.title'];
        } else {
            var pageKey = document.body.getAttribute('data-page-title-key');
            if (pageKey && dict[pageKey]) {
                document.title = dict[pageKey] + ' — StarConnect Solutions';
            }
        }

        // active state in lang menu
        var options = document.querySelectorAll('.lang-option');
        options.forEach(function(opt) {
            if (opt.getAttribute('data-lang') === lang) {
                opt.classList.add('active');
            } else {
                opt.classList.remove('active');
            }
        });

        localStorage.setItem(STORAGE_KEY, lang);
    }

    // expose
    window.setLang = function(lang) { applyLang(lang); };
    window.getLang = function() { return localStorage.getItem(STORAGE_KEY) || detectLang(); };

    // init on DOMContentLoaded
    function init() {
        applyLang(detectLang());

        // bind lang options
        var options = document.querySelectorAll('.lang-option');
        options.forEach(function(opt) {
            opt.addEventListener('click', function(e) {
                e.preventDefault();
                var lang = this.getAttribute('data-lang');
                if (lang) applyLang(lang);
            });
        });
    }

    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', init);
    } else {
        init();
    }
})();
